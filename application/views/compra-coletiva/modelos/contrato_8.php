<p class="c21"><span class="c0">Os recursos financeiros necess&aacute;rios para execu&ccedil;&atilde;o deste </span><span class="c3">CONTRATO</span><span class="c0">&nbsp;ser&aacute; proveniente do </span><span class="c3">COMPRADOR</span><span class="c0">&nbsp;</span><span class="c3">AUTORIZADO</span><span class="c0">, o qual dever&aacute; efetuar os pagamentos NOS VALORES E NAS DATAS descritas </span><span class="c3">no Anexo II</span><span class="c0">&nbsp;deste Instrumento, em benef&iacute;cio do </span><span
