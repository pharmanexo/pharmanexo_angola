<p class="c9"><span class="c1 c0"></span></p>
<p class="c25"><span class="c0">Constituem obriga&ccedil;&otilde;es do </span><span class="c3">COMPRADOR AUTORIZADO</span><span class="c1 c0">:</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c23 c45"><span class="c0">I &ndash; Cumprir todas suas obriga&ccedil;&otilde;es para com o </span><span class="c3">VENDEDOR;</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<ol class="c13 lst-kix_list_12-1 start" start="35">
	<li class="c23 c46 c47"><span class="c0">&ndash; Avaliar de forma t&eacute;cnica os anexos deste </span><span class="c3">CONTRATO</span><span class="c1 c0">, visando a aprova&ccedil;&atilde;o das caracter&iacute;sticas t&eacute;cnicas das M&aacute;scaras e as informa&ccedil;&otilde;es financeiras da opera&ccedil;&atilde;o.</span></li>
</ol>
<p class="c9"><span class="c1 c0"></span></p>
<ol class="c13 lst-kix_list_13-0 start" start="61">
	<li class="c28 c32"><span class="c1 c0">&ndash; Estar em anu&ecirc;ncia com os prazos definidos neste CONTRATO;</span></li>
</ol>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c21"><span class="c0">IV &ndash; Efetuar os pagamentos conforme descrito neste CONTRATO dentro dos prazos especificados no </span><span class="c1 c3">ANEXO II.</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c21"><span class="c1 c0">V &ndash; Fornecer ao VENDEDOR os documentos necess&aacute;rios para aquisi&ccedil;&atilde;o de insumos m&eacute;dicos, em conson&acirc;ncia com a legisla&ccedil;&atilde;o brasileira e normas da ANVISA;</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c2"><span class="c3">CL&Aacute;USULA NONA - DAS INFRA&Ccedil;&Otilde;ES E PENALIDADES</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c21"><span class="c0">O descumprimento de qualquer disposi&ccedil;&atilde;o legal, regulamentar ou dos termos e condi&ccedil;&otilde;es expressos ou impl&iacute;citos neste </span><span class="c3">CONTRATO,</span><span class="c1 c0">&nbsp;resultar&aacute; na aplica&ccedil;&atilde;o de penalidade de multa referente a 10 % (dez por cento) do valor total transacionado entre as Partes, a ser pago pela parte infratora as demais Partes lesadas.</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c23 c41"><span class="c3">Par&aacute;grafo &uacute;nico:</span><a id="id.2et92p0"></a><span class="c3">&nbsp;</span><span class="c0">Para aplica&ccedil;&atilde;o da penalidades de que trata o </span><span class="c0 c18">caput</span><span class="c1 c0">&nbsp;desta Cl&aacute;usula, ser&aacute; exclu&iacute;do situa&ccedil;&otilde;es motivadas por dificuldade de contrata&ccedil;&atilde;o de voo na modalidade de fretamento ocasionado pela inexist&ecirc;ncia da oferta de empresa a ser contratada para este fim e/ou colapso da malha aerovi&aacute;ria e/ou superfaturamento dos custos de transporte o que inviabilizaria a opera&ccedil;&atilde;o comercial.</span>
</p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c2"><span class="c3">CL&Aacute;USULA D&Eacute;CIMA - DA EXTIN&Ccedil;&Atilde;O DA AUTORIZA&Ccedil;&Atilde;O</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c21"><span class="c0">A </span><span class="c3">AUTORIZA&Ccedil;&Atilde;O</span><span class="c0">&nbsp;poder&aacute; ser extinta por ren&uacute;ncia, fal&ecirc;ncia ou extin&ccedil;&atilde;o do </span><span class="c3">VENDEDOR</span><span class="c0">&nbsp;e/ou </span><span class="c3">COMPRADOR AUTORIZADO</span><span
		class="c1 c0">, por meio de anula&ccedil;&atilde;o, cassa&ccedil;&atilde;o ou declara&ccedil;&atilde;o de inidoneidade, observado o devido processo legal e a ampla defesa.</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c11"><span class="c3">Par&aacute;grafo primeiro: </span><span class="c0">A penalidade de anula&ccedil;&atilde;o ser&aacute; aplicada quando a </span><span class="c3">AUTORIZA&Ccedil;&Atilde;O</span><span class="c0">&nbsp;estiver eivada de v&iacute;cios que a tornem ilegal ou quando constatado que o </span><span class="c3">COMPRADOR AUTORIZADO</span><span class="c0">&nbsp;e/ou </span><span class="c3">VENDEDOR</span><span class="c1 c0">&nbsp;apresentou documenta&ccedil;&atilde;o irregular ou usou de m&aacute; f&eacute; nas informa&ccedil;&otilde;es prestadas, independentemente de outras penalidades cab&iacute;veis.</span>
</p>
<p class="c7 c15"><span class="c1 c0"></span></p>
<p class="c23 c41"><span class="c3">Par&aacute;grafo segundo: </span><span class="c0">A penalidade de cassa&ccedil;&atilde;o da autoriza&ccedil;&atilde;o poder&aacute; ser aplicada, a crit&eacute;rio do </span><span class="c3">VENDEDOR</span><span class="c1 c0">, considerando a gravidade do fato, quando:</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c21"><span class="c0">I &ndash; N&atilde;o forem cumpridos os pagamentos nos prazos assinalados neste</span><span class="c3">&nbsp;CONTRATO;</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<ol class="c13 lst-kix_list_19-0 start" start="35">
	<li class="c23 c32 c46"><span class="c1 c0">&ndash; N&atilde;o for atendidas as exig&ecirc;ncias documentais obrigat&oacute;rias por lei;</span></li>
</ol>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c23 c39"><span class="c3">Par&aacute;grafo terceiro: </span><span class="c0">A penalidade de declara&ccedil;&atilde;o de inidoneidade da </span><span class="c3">VENDEDORA e/ou COMPRADOR AUTORIZADO</span><span class="c1 c0">&nbsp;ser&aacute; aplicada nos seguintes casos:</span></p><a id="id.tyjcwt"></a>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c17"><span class="c0">I &nbsp;&ndash; &nbsp;pr&aacute;tica &nbsp;de &nbsp;atos &nbsp;il&iacute;citos &nbsp;visando &nbsp;frustrar &nbsp;os &nbsp;objetivos &nbsp;da &nbsp;execu&ccedil;&atilde;o &nbsp;do </span><span class="c3">CONTRATO</span><span class="c1 c0">;</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c2"><span class="c1 c0">II &ndash; apresenta&ccedil;&atilde;o de informa&ccedil;&otilde;es e dados falsos;</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c21"><span class="c1 c0">III &ndash; pr&aacute;tica de abuso de poder econ&ocirc;mico ou infra&ccedil;&atilde;o &agrave;s normas para defesa da concorr&ecirc;ncia, apuradas e julgadas na forma da legisla&ccedil;&atilde;o aplic&aacute;vel.</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c19"><span class="c3">Par&aacute;grafo quarto: </span><span class="c0">A declara&ccedil;&atilde;o de inidoneidade implicar&aacute;, necessariamente, na cassa&ccedil;&atilde;o da </span><span class="c3">AUTORIZA&Ccedil;&Atilde;O</span><span class="c1 c0">.</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c2"><span class="c3">CL&Aacute;USULA D&Eacute;CIMA PRIMEIRA - DA EXECU&Ccedil;&Atilde;O DO OBJETO</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c21"><span class="c0">Incumbe ao </span><span class="c3">VENDEDOR</span><span class="c0">&nbsp;executar a opera&ccedil;&atilde;o de aquisi&ccedil;&atilde;o, </span><span class="c3">DE M&Aacute;SCARA TRIPLA DESCART&Aacute;VEL MARCA MEDIX, COM ANVISA</span><span class="c0">, </span><span class="c3">REGISTRO N. 80495510006, </span><span class="c1 c0">podendo faz&ecirc;-lo direta ou indiretamente, assegurando o cumprimento das normas legais, regulamentares e t&eacute;cnicas aplic&aacute;veis, especialmente as relativas &agrave; seguran&ccedil;a das pessoas, bens e instala&ccedil;&otilde;es.</span>
</p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c23 c31"><span class="c3">Par&aacute;grafo primeiro: </span><span class="c0">A aquisi&ccedil;&atilde;o das m&aacute;scaras somente se dar&aacute; se cumprido os requisitos descritos na cl&aacute;usula primeira, par&aacute;grafo terceiro deste </span><span class="c3">CONTRATO</span><span class="c0">, caso o VENDEDOR e/ou INTERMEDI&Aacute;RIO n&atilde;o logre &ecirc;xito no cumprimento das exig&ecirc;ncias at&eacute; a data limite de vig&ecirc;ncia, este CONTRATO se extinguir&aacute; imediatamente, e todos valores depositados pelo </span><span
		class="c3">COMPRADOR AUTORIZADO</span><span class="c0">&nbsp;ser&aacute; devolvido em no m&aacute;ximo 24 (vinte e quatro) horas, de forma integral sem corre&ccedil;&otilde;es e/ou multas, pelo </span><span class="c3">VENDEDOR</span><span class="c1 c0">.</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c2"><span class="c3">CL&Aacute;USULA D&Eacute;CIMA SEGUNDA - DA EXTIN&Ccedil;&Atilde;O DO CONTRATO</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c21"><span class="c0">No caso de extin&ccedil;&atilde;o do CONTRATO por prazo, ocasionado por descumprimento das exig&ecirc;ncias contratuais no que tange o quantitativo m&iacute;nimo de M&Aacute;SCARAS necess&aacute;rias para concretiza&ccedil;&atilde;o da aquisi&ccedil;&atilde;o</span><span class="c3">,</span><span class="c0">&nbsp;fica desde j&aacute; acordado entre as Partes, que </span><span class="c12 c0">n&atilde;o haver&aacute; qualquer responsabiliza&ccedil;&atilde;o legal e/ou administrativa</span><span
		class="c0">&nbsp;do </span><span class="c3">VENDEDOR</span><span class="c0">&nbsp;e/ou </span><span class="c3">INTERMEDI&Aacute;RIO</span><span class="c1 c0">, bem como haver&aacute; a isen&ccedil;&atilde;o de responsabilidade direta e/ou solid&aacute;ria no caso de danos materiais, morais e ou risco &agrave; vida humana.</span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c9"><span class="c1 c0"></span></p>
<p class="c2"><span class="c3">CL&Aacute;USULA D&Eacute;CIMA TERCEIRA - DOS RECURSOS</span></p>
